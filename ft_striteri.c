#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;

	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}

// applique f a chaque char
// f recoit index ET POINTEUR vers char
// diff avec strmapi: MODIFIE original (pas de malloc)
// f(i, &s[i]) permet de modifier direct

