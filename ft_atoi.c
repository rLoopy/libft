#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	signe;
	int	res;

	i = 0;
	signe = 1;
	res = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		signe = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	return (res * signe);
}

// string -> int
// 1. skip espaces (32, 9-13)
// 2. gere signe (+ ou -)
// 3. convertit: res = res*10 + (str[i]-'0')
// 4. applique signe a la fin